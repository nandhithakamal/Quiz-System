    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String ques = quesTBox.getText();
        String opt1 = opt1TBox.getText();
        String opt2 = opt2TBox.getText();
        String opt3 = opt3TBox.getText();
        String opt4 = opt4TBox.getText();
        String corrOpt = correctTBox.getText();
        i += 1;
        try{
            conn = DBConnection.getInstance().connectUsers();
            st = conn.createStatement();
            query = "insert into " + quizName + "_" + username + " values(" + i + ", '" + ques + "', '" + opt1 + "', '" +  opt2 + "', '" + opt3 + "', '" +  opt4 + "', '" + corrOpt + "');";
            stat = st.executeUpdate(query);
            if(stat != 0){
                JOptionPane jop = new JOptionPane();
                JOptionPane.showMessageDialog(new JFrame(), "Question " + i + " added.","Successful", JOptionPane.INFORMATION_MESSAGE);
                jop.setVisible(true);
                quesTBox.setText("");
                opt1TBox.setText("");
                opt2TBox.setText("");
                opt3TBox.setText("");
                opt4TBox.setText("");
                correctTBox.setText("");
                
            }  
            else{
                i--;
                JOptionPane jop = new JOptionPane();
                JOptionPane.showMessageDialog(new JFrame(), "Unable to add question.","Error", JOptionPane.ERROR_MESSAGE);
                jop.setVisible(true);
                quesTBox.setText("");
                opt1TBox.setText("");
                opt2TBox.setText("");
                opt3TBox.setText("");
                opt4TBox.setText("");
                correctTBox.setText("");
                
            }
            
            conn.close();
        } catch (IOException | ClassNotFoundException | SQLException ex) {
                Logger.getLogger(OpenPage.class.getName()).log(Level.SEVERE, null, ex);
        }  
        
    }//GEN-LAST:event_addButtonActionPerformed

