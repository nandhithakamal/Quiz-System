    private void backButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backButtonActionPerformed
        // TODO add your handling code here: 
        i--;
        try{
            conn = DBConnection.getInstance().connectUsers();
            st = conn.createStatement();
            query = "select * from " + quizName + "_" + username + " where no = " + i;
            ResultSet rset = st.executeQuery(query);
            while(rset.next()){
                String question = rset.getString("question");
                quesTBox.setText(question);
                String opt1 = rset.getString("opt1");
                opt1TBox.setText(opt1);
                String opt2 = rset.getString("opt2");
                opt2TBox.setText(opt2);
                String opt3 = rset.getString("opt3");
                opt3TBox.setText(opt3);
                String opt4 = rset.getString("opt4");
                opt4TBox.setText(opt4);
                String correct = rset.getString("rightOpt");
                correctTBox.setText(correct);
                
                
            }
            
            conn.close();
                   
       
        } catch (SQLException | IOException | ClassNotFoundException ex) {
            Logger.getLogger(CreatingQuizzes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_backButtonActionPerformed

